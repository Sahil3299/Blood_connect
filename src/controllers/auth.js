const jwt = require('jsonwebtoken');
const User = require('../models/user');

const DEFAULT_SECRET = 'bloodconnect_default_secret_key_2024';

const generateToken = (user) => {
  const secret = process.env.JWT_SECRET || DEFAULT_SECRET;
  return jwt.sign(
    { id: user._id, name: user.name, email: user.email },
    secret,
    { expiresIn: '7d' }
  );
};

const signup = async (req, res) => {
  try {
    const { name, email, password } = req.body;
    if (!name || !email || !password) return res.status(400).json({ message: 'Missing required fields' });

    const existing = await User.findOne({ email });
    if (existing) return res.status(409).json({ message: 'Email already in use' });

    const user = new User({ name, email, password });
    await user.save();

    const token = generateToken(user);
    res.status(201).json({ 
      message: 'Signup successful', 
      token,
      user: { id: user._id, name: user.name, email: user.email }
    });
  } catch (err) {
    console.error('Signup error:', err);
    res.status(500).json({ message: 'Server error during signup' });
  }
};

const login = async (req, res) => {
  try {
    const { email, password } = req.body;
    if (!email || !password) return res.status(400).json({ message: 'Missing email or password' });

    const user = await User.findOne({ email });
    if (!user) return res.status(401).json({ message: 'Invalid credentials' });

    const match = await user.comparePassword(password);
    if (!match) return res.status(401).json({ message: 'Invalid credentials' });

    const token = generateToken(user);
    res.status(200).json({ 
      message: 'Login successful', 
      token,
      user: { id: user._id, name: user.name, email: user.email }
    });
  } catch (err) {
    console.error('Login error:', err);
    res.status(500).json({ message: 'Server error during login' });
  }
};

const logout = (req, res) => {
  // For JWT, we just tell the client to remove the token
  // No server-side action needed as JWT is stateless
  res.json({ message: 'Logged out' });
};

const me = (req, res) => {
  // Check if user is attached from JWT middleware
  if (req.user) {
    return res.status(200).json({ user: req.user });
  }
  return res.status(401).json({ message: 'Not authenticated' });
};

module.exports = { signup, login, logout, me };
